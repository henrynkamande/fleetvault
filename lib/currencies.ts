export type CurrencyOption = {
  code: string
  label: string
}

export function getCurrencyCodes(): string[] {
  const intlWithCurrencyList = Intl as typeof Intl & {
    supportedValuesOf?: (key: 'currency') => string[]
  }

  return (intlWithCurrencyList.supportedValuesOf?.('currency') ?? FALLBACK_CURRENCY_CODES)
    .filter((code, index, codes) => codes.indexOf(code) === index)
    .sort((a, b) => a.localeCompare(b))
}

export function getCurrencyOptions(): CurrencyOption[] {
  const displayNames =
    typeof Intl.DisplayNames === 'function' ? new Intl.DisplayNames(['en'], { type: 'currency' }) : null

  return getCurrencyCodes().map((code) => ({
    code,
    label: displayNames?.of(code) ?? code,
  }))
}

export function normalizeCurrency(value: string | null | undefined): string {
  const currency = value?.trim().toUpperCase()
  return currency && /^[A-Z]{3}$/.test(currency) ? currency : 'USD'
}
